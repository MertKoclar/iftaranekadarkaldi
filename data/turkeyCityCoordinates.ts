export const getCityCoordinates = (cityName: string): { latitude: number; longitude: number } | null => {
  return cityCoordinates[cityName] || null;
};

export const getDistrictCoordinates = (cityName: string, districtName: string): { latitude: number; longitude: number } | null => {
  const key = `${cityName} - ${districtName}`;
  return districtCoordinates[key] || null;
};

export const getCoordinates = (cityName: string, districtName?: string): { latitude: number; longitude: number } | null => {
  // Önce ilçe koordinatını kontrol et
  if (districtName) {
    const districtCoord = getDistrictCoordinates(cityName, districtName);
    if (districtCoord) {
      return districtCoord;
    }
  }
  
  // İlçe koordinatı yoksa il koordinatını kullan
  return getCityCoordinates(cityName);
};

